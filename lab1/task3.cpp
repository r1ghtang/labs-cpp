#include <iostream>
using namespace std;

// Функция обмена по значению
// Здесь меняются только копии переменных,
// поэтому в main значения не изменятся
void obmen1(int a, int b) {
    int temp = a;
    a = b;
    b = temp;
}

// Функция обмена через указатели
// Здесь передаются адреса переменных,
// поэтому меняются сами исходные значения
void obmen2(int* a, int* b) {
    int temp = *a;
    *a = *b;
    *b = temp;
}

// Функция обмена через ссылки
// Здесь параметры ссылаются на исходные переменные,
// поэтому значения тоже реально меняются
void obmen3(int& a, int& b) {
    int temp = a;
    a = b;
    b = temp;
}

int main() {
    int a = 2, b = 5;

    // Выводим исходные значения
    cout << "Before swap: a = " << a << ", b = " << b << endl;

    // Обмен по значению
    // После вызова a и b в main не изменятся
    obmen1(a, b);
    cout << "After obmen1: a = " << a << ", b = " << b << endl;

    // Обмен через указатели
    // Передаём адреса переменных
    obmen2(&a, &b);
    cout << "After obmen2: a = " << a << ", b = " << b << endl;

    // Чтобы снова показать обмен через ссылки,
    // вернём значения обратно
    obmen3(a, b);
    cout << "After obmen3: a = " << a << ", b = " << b << endl;

    return 0;
}
